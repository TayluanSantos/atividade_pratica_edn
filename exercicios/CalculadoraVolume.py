#Calcule o volume de uma caixa retangular. Use três variáveis para representar o comprimento, a largura e a altura. Multiplique os três valores e exiba o resultado com a unidade cm³.

def calcular_volume_retangulo(comprimento,largura,altura):

    volume_calculado = comprimento * largura * altura
    print(f"O volume do retangulo é igual a: {volume_calculado} cm3")

calcular_volume_retangulo (5,4,3)