# Calculadora de Consumo de Combustível
# Objetivo: Desenvolver um programa que calcula o consumo médio de combustível de um veículo

distancia_percorrida =  300 
combustivel_gasto = 25 

consumo_medio = distancia_percorrida / combustivel_gasto

print(f"Distancia percorrida: {distancia_percorrida} km")
print(f"Combustivel gasto: {combustivel_gasto} litros")
print(f"Consumo médio de combustível: {consumo_medio:.2f} km/l")