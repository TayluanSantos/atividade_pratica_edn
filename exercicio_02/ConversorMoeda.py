# Conversor de Moeda
# Objetivo: Criar um programa que converte um valor em reais para d√≥lares e euros.

valor_reais = 100.00 
taxa_dolar = 5.20 
taxa_euro = 6.15

valor_convertido_dolar = valor_reais / taxa_dolar;
valor_convertido_euro = valor_reais / taxa_euro;

print(f"Valor em reais: {valor_reais:.2f}")
print(f"Valor convertido de reais para dolar: {valor_convertido_dolar:.2f}")
print(f"Valor convertido de reais para euro: {valor_convertido_euro:.2f}")

